     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Contact  $contact
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        request()->validate([
            "titre2"=>"required",
            "titre3"=>"required",
            "ville"=>"required",
            "rue"=>"required",
            "phone"=>"required",
            "mail"=>"required",
            "button"=>"required",
        ]);
        $contact=Contact::find($id);
        $contact->titre2 = $request->titre2;
        $contact->titre3 = $request->titre3;
        $contact->ville = $request->ville;
        $contact->rue = $request->rue;
        $contact->phone = $request->phone;
        $contact->mail = $request->mail;
        $contact->button = $request->button;
        $contact->save();
        return redirect()->back()->with('success','Vos données ont bien été mis à jour !');
    }

